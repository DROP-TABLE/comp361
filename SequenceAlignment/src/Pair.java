

public class Pair {

	private String firstElem;
	private String secondElem;
	
	public Pair(String firstElem, String secondElem) {
		super();
		this.firstElem = firstElem;
		this.secondElem = secondElem;
	}

	public String getFirstElem() {
		return firstElem;
	}

	public void setFirstElem(String firstElem) {
		this.firstElem = firstElem;
	}

	public String getSecondElem() {
		return secondElem;
	}

	public void setSecondElem(String secondElem) {
		this.secondElem = secondElem;
	}
	
}
